package com.callor.hello;
/*
 * 두개의 숫자 30과40의 4칙연산을 수행하여 결과를 console 에 출력하시오
 */
public class NumberOp {
	
	public static void main(String[] args) {
		System.out.println(30 + 40);
		System.out.println(30 - 40);
		System.out.println(30 * 40);
		
		//정수의 나누눗셈은 몫은 구하는 연산
		System.out.println(30 / 40);
		//정수의 나머지 연산
		 System.out.println(30 % 40);
	}	
	//실수와 실수를 나눗셈 연산
	//연산결과도



	
}
